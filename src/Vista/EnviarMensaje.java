    private void cancelarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelarBtnActionPerformed
        // TODO add your handling code here:
        VentanaHome.controlSesion.restart();
        if(this.panelAdmin != null){
            clearInfo();
            this.panelAdmin.reset();
        }else{
            clearInfo();
            this.panelMedico.reset();
        }
    }//GEN-LAST:event_cancelarBtnActionPerformed

    private void enviarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enviarBtnActionPerformed
        // TODO add your handling code here:
        VentanaHome.controlSesion.restart();
        Mensaje msg = new Mensaje(remitente, destinatario, Calendar.getInstance(), this.mensajeTA.getText());
        if(this.panelAdmin != null){
            VentanaHome.Sistema_principal.getMensajesParaMed().add(msg);
            clearInfo();
            this.panelAdmin.reset();
            JOptionPane.showMessageDialog(this, "Mensaje enviado", "Mensaje enviado correctamente", JOptionPane.INFORMATION_MESSAGE);
        }else{
            if(this.panelMedico.isRedactar()){
                this.destinatario = this.panelMedico.getDestinatario();
                msg.setIdDestinatario(this.destinatario);
            }
            if(this.destinatario != -1){
                VentanaHome.Sistema_principal.getMensajesParaAdmin().add(msg);
                clearInfo();
                this.panelMedico.reset();
                JOptionPane.showMessageDialog(this, "Mensaje enviado", "Mensaje enviado correctamente", JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(this, "Ingrese un ID de destinatario v√°lido", "ID destinatario incorrecto", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_enviarBtnActionPerformed

