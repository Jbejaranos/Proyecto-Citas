    public CanalContactoMedico() {
        initComponents();
        this.remitenteTF.setEditable(false);
        this.fechaTF.setEditable(false);
        this.mensajeTA.setEditable(false);
        this.panelRespuesta = new EnviarMensaje(Login.med.getIdentificacion());
        this.panelRespuesta.setPanelMedico(this);
        this.panelRespuesta.setBackground(this.panelMensaje.getBackground());
        this.mensajesRecibidos = new ArrayList<>();
        
        for(Mensaje msg: VentanaHome.Sistema_principal.getMensajesParaMed()){
            if(msg.getIdDestinatario() == Login.med.getIdentificacion()){
                this.mensajesRecibidos.add(msg);
            }
            //System.out.println(msg.getIdDestinatario());
        }
        
        String[] model = new String[this.mensajesRecibidos.size()];
        
        for(int i = 0; i < this.mensajesRecibidos.size(); i++){
            model[i] = Long.toString(this.mensajesRecibidos.get(i).getIdRemitente());
        }
        
        this.listaMensajes.setListData(model);
        
        this.listaMensajes.addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                if(remitenteLabel.getText().equals("Remitente:")){
                    seleccion = listaMensajes.getSelectedIndex();
                    Mensaje msg = mensajesRecibidos.get(seleccion);
                    long idRemitente = msg.getIdRemitente();
                    String nombreRemitente = "";
                    Calendar fecha = msg.getFecha();
                    String mensaje = msg.getMensaje();
                    for(Administrador ad: VentanaHome.Sistema_principal.getAdministradores()){
                        if(idRemitente == ad.getIdentificacion()){
                            nombreRemitente = ad.getNombre();
                            break;
                        }
                    }
                    prevInfo = nombreRemitente;
                    remitenteTF.setText(nombreRemitente);
                    fechaTF.setText(CitasPaciente.getTextoFecha(fecha));
                    mensajeTA.setText(mensaje);
                }
            }
        });
    }

