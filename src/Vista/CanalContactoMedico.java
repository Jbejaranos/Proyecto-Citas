    private void redactarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_redactarBtnActionPerformed
        // TODO add your handling code here:
        VentanaHome.controlSesion.restart();
        this.redactar = true;
        this.remitenteLabel.setText("Destinatario:");
        this.remitenteTF.setText("");
        this.remitenteTF.setEditable(true);
        this.fechaLabel.setVisible(false);
        this.fechaTF.setVisible(false);
        this.panelRespuesta.setSize(this.panelMensaje.getWidth(), this.panelMensaje.getHeight());
        this.panelRespuesta.setLocation(0, 0);
        this.panelMensaje.removeAll();
        this.panelMensaje.add(this.panelRespuesta);
        this.panelMensaje.revalidate();
        this.panelMensaje.repaint();
    }//GEN-LAST:event_redactarBtnActionPerformed

    private void responderBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_responderBtnActionPerformed
        // TODO add your handling code here:
        VentanaHome.controlSesion.restart();
        if(this.seleccion != -1){
            this.panelMensaje.removeAll();
            this.panelRespuesta.setSize(this.panelMensaje.getWidth(), this.panelMensaje.getHeight());
            this.panelRespuesta.setLocation(0, 0);
            this.panelRespuesta.setDestinatario(this.mensajesRecibidos.get(this.seleccion).getIdRemitente());
            this.panelMensaje.add(this.panelRespuesta);
            this.panelMensaje.revalidate();
            this.panelMensaje.repaint();
        }else{
            JOptionPane.showMessageDialog(this, "Por favor seleccione un mensaje", "Mensaje no seleccionado", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_responderBtnActionPerformed

    public long getDestinatario(){
        if(this.remitenteTF.getText().isEmpty())
            return -1;
        return Long.parseLong(this.remitenteTF.getText());
    }
    
    public boolean isRedactar(){
        return this.redactar;
    }
    
    public void reset(){
        this.redactar = false;
        this.remitenteLabel.setText("Remitente:");
        this.remitenteTF.setText(this.prevInfo);
        this.remitenteTF.setEditable(false);
        this.fechaLabel.setVisible(true);
        this.fechaTF.setVisible(true);
        this.panelMensaje.removeAll();
        this.panelMensaje.add(mensajeLabel);
        this.panelMensaje.add(jScrollPane2);
        this.panelMensaje.add(redactarBtn);
        this.panelMensaje.add(responderBtn);
        this.panelMensaje.revalidate();
        this.panelMensaje.repaint();
    }

