    private void guardarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarBtnActionPerformed
        // TODO add your handling code here:
        String numero = this.numeroTF.getText();
        String nombre = this.nombreTF.getText();
        String sede = this.sedeTF.getText();
        boolean checkNum = numero.matches("[0-9]+");
        boolean checkNombre = !nombre.isEmpty();
        boolean checkSede = !sede.isEmpty();
        boolean checkAll = checkNum && checkNombre && checkSede;
        
        if(checkAll){
            VentanaHome.Sistema_principal.getConsultorios().get(index).setNumero(Integer.parseInt(numero));
            VentanaHome.Sistema_principal.getConsultorios().get(index).setNombre(nombre);
            VentanaHome.Sistema_principal.getConsultorios().get(index).setSede(sede);
            this.parentPanel.actualizar();
        }else{
            JOptionPane.showMessageDialog(this, "Por favor ingrese la información correctamente");
        }
    }//GEN-LAST:event_guardarBtnActionPerformed

