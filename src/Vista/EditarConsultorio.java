    public void actualizarInfo(int index){
        this.index = index;
        Consultorio cons = VentanaHome.Sistema_principal.getConsultorios().get(index);
        this.numeroTF.setText(Integer.toString(cons.getNumero()));
        this.nombreTF.setText(cons.getNombre());
        this.sedeTF.setText(cons.getSede());
    }
    
