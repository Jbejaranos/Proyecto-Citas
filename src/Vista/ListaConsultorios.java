    public void inicializarLista(){
        ArrayList<Consultorio> consultorios = VentanaHome.Sistema_principal.getConsultorios();
        DefaultTableModel model = (DefaultTableModel) this.consTable.getModel();
        for(Consultorio c: consultorios){
            int num = c.getNumero();
            String nombre = c.getNombre();
            String sede = c.getSede();
            model.addRow(new Object[]{num, nombre, sede});
        }
    }
    
    public void actualizarLista(){
        ArrayList<Consultorio> consultorios = VentanaHome.Sistema_principal.getConsultorios();
        DefaultTableModel model = (DefaultTableModel) this.consTable.getModel();
        for(int i = 0; i < model.getRowCount(); i++){
            int num = consultorios.get(i).getNumero();
            String nombre = consultorios.get(i).getNombre();
            String sede = consultorios.get(i).getSede();
            model.setValueAt(num, i, 0);
            model.setValueAt(nombre, i, 1);
            model.setValueAt(sede, i, 2);
        }
    }

