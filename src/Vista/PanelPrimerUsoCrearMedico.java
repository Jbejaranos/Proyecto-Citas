    public Calendar getFechaNacimiento(){
        String fecha = this.fechaTF.getText();
        String[] datos = fecha.split("/");
        
        int dia = Integer.parseInt(datos[0]);
        int mes = Integer.parseInt(datos[1]);
        int year = Integer.parseInt(datos[2]);
        
        GregorianCalendar fechaObtenida = new GregorianCalendar(year, mes - 1, dia);
        
        return fechaObtenida;
    }
    
    public long getIdentificacion(){
        return Long.parseLong(this.idTF.getText());
    }
    
    public String getEspecialidad(){
        return (String) this.espComboBox.getSelectedItem();
    }
    
    public String getPassword(){
        String password = "";
        for(char c: this.passwordTF.getPassword())
            password += c;
        return password;
    }
    
    public String getPasswordEncrypted(){
        return Modelo.Encriptacion.encriptar(getPassword());
    }
    
    public boolean confirmarInformacion(){
        boolean nombreIsCorrect = !this.nombreTF.getText().isEmpty();
        String genero = (String) this.generoComboBox.getSelectedItem();
        boolean generoIsCorrect = !genero.isEmpty();
        boolean fechaIsCorrect = this.fechaTF.getText().matches("\\d{2}/\\d{2}/\\d{4}");
        boolean idIsCorrect = this.idTF.getText().matches("[0-9]+");
        String password = getPassword();
        boolean passwordIsCorrect = !password.isEmpty();
        return nombreIsCorrect && generoIsCorrect && fechaIsCorrect && idIsCorrect && passwordIsCorrect;
    }

